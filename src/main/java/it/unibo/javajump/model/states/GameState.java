package it.unibo.javajump.model.states;

/**
 * The enum that specifies the existing Game states.
 */
public enum GameState {
    /**
     * "Menu" game state.
     */
    MENU,
    /**
     * "In game" game state.
     */
    IN_GAME,
    /**
     * "Pause" game state.
     */
    PAUSE,
    /**
     * "Game over" game state.
     */
    GAME_OVER,
}
