package it.unibo.javajump.model.states;

/**
 * The enum Game state.
 */
public enum GameState {
    /**
     * Menu game state.
     */
    MENU,
    /**
     * In game game state.
     */
    IN_GAME,
    /**
     * Pause game state.
     */
    PAUSE,
    /**
     * Game over game state.
     */
    GAME_OVER,
}
